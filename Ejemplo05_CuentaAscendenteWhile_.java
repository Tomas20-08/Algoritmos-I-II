public class Ejemplo05_CuentaAscendenteWhile_ {

    public static void main(String[] args) {
        
        int x;
        x=1;
        
        while (x<=100) {
            System.out.println(x);
            System.out.println(" - ");
            x = x + 1;
        }

    }
    
}
