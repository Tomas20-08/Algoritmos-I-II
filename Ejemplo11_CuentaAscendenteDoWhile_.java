//Cuenta Asendente dependiendo del usuario (Uso de "Do While")

package CiclosBucles;

public class Ejemplo11_CuentaAscendenteDoWhile_ {

    public static void main(String[] args) {
        
        //Variables
        
        int cont = 0;
        
        //Ciclo/Bucle
                
        do {
            
            //Salida
            
            System.out.println("El contador es:" + cont);
            cont++;
        } while (cont <= 100);
 
    }
    
}
